#Writing code for each arithmetic operation and calling function with print defined in function
a = 5
b = 4

#Defining Addition and printing
def addition():
    addition = (a + b)
    print(addition)

#Defining Subtraction and printing
def subtraction():
    subtraction = (a - b)
    print(subtraction)

#Defining Multiplication and printing
def multiplication():
    multiplication = (a * b)
    print(multiplication)

#Defining Division and printing
def division():
    division = (a / b)
    print(division)

#Defining main menu to call all operations and printing
def main_menu():
    addition()
    subtraction()
    multiplication()
    division()

#Calling defined operation
main_menu()

